-- Create series_monthly_aggregate_fact Table (Aggregation over Month and Series, with Customer)
CREATE TABLE series_monthly_aggregate_fact (
    series_monthly_agg_id  INT PRIMARY KEY,         
    series_id              INT NOT NULL,            
    month_id               INT NOT NULL,            
    customer_id            INT NOT NULL,            
    total_view_count       INT DEFAULT 0,          
    total_viewing_duration INT DEFAULT 0,          
    avg_engagement_score   DECIMAL(5,2) DEFAULT 0,  
    FOREIGN KEY (series_id)   REFERENCES dim_series(series_id),
    FOREIGN KEY (month_id)    REFERENCES dim_month(month_id),
    FOREIGN KEY (customer_id) REFERENCES Customer_dimension(customer_id)
);

INSERT INTO series_monthly_aggregate_fact (
    series_monthly_agg_id,
    series_id,
    month_id,
    customer_id,
    total_view_count,
    total_viewing_duration,
    avg_engagement_score
)
SELECT 
    ROW_NUMBER() OVER (ORDER BY ds.series_id, dm.month_id, cef.customer_id) AS series_monthly_agg_id,
    ds.series_id,                     
    dm.month_id,                      
    cef.customer_id,
    SUM(cef.view_count) AS total_view_count,
    SUM(cef.viewing_duration) AS total_viewing_duration,
    AVG(cef.engagement_score) AS avg_engagement_score
FROM customer_engagement_fact cef
JOIN content_dimension cd 
    ON cef.content_id = cd.content_id
JOIN dim_series ds 
    ON cd.series_name = ds.series_name   
JOIN Time_dimension t 
    ON cef.time_id = t.time_id
JOIN dim_month dm 
    ON dm.month_record = t.month_record 
       AND dm.quarter_record = t.quarter_record 
       AND dm.year_record = t.year_record
GROUP BY ds.series_id, dm.month_id, cef.customer_id;

