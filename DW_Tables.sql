    month_record VARCHAR(20),
    quarter_record VARCHAR(5),
    year_record INT
);

INSERT INTO dim_month (month_id, month_record, quarter_record, year_record)
SELECT 
    ROW_NUMBER() OVER (ORDER BY year_record, month_record) AS month_id,
    month_record,
    quarter_record,
    year_record
FROM (
    SELECT DISTINCT month_record, quarter_record, year_record
    FROM Time_dimension
) AS distinct_months;

