--Create Monthly_aggregate_fact Table
CREATE TABLE Monthly_aggregate_fact (
    monthly_agg_id INT PRIMARY KEY,
    customer_id INT,
    total_subscriptions INT,
    total_unsubscriptions INT,
    total_customers INT,
    month_id INT,
    FOREIGN KEY (customer_id) REFERENCES Customer_dimension (customer_id),
    FOREIGN KEY (month_id) REFERENCES dim_month(month_id)
);

INSERT INTO Monthly_aggregate_fact (monthly_agg_id, customer_id, total_subscriptions, total_unsubscriptions, total_customers, month_id)
SELECT 
    ROW_NUMBER() OVER (ORDER BY dm.month_id, t.year_record) AS monthly_agg_id,
    s.customer_id,
    COUNT(CASE WHEN s.subscription_status = 'Active' THEN 1 END) AS total_subscriptions,
    COUNT(CASE WHEN s.subscription_status = 'Inactive' THEN 1 END) AS total_unsubscriptions,
    COUNT(DISTINCT s.customer_id) AS total_customers,
    dm.month_id
FROM Subscription_fact s
JOIN Time_dimension t 
    ON s.time_id = t.time_id
JOIN dim_month dm 
    ON dm.month_record = t.month_record
GROUP BY s.customer_id, dm.month_id, t.year_record;
